static void render_right(void) {
    // QMK Logo and version information

    render_qmk_logo();
    
    // oled_write_P(PSTR("Kyria rev1.3\n\n"), false);

    // Host Keyboard Layer Status
    oled_write_P(PSTR("Layer: "), false);
    switch (get_highest_layer(layer_state)) { // each case has a base cost of 7 bytes, and each character is 1 byte
        case _COLEMAK: // 16 bytes
            oled_write_P(PSTR("Colemak\n"), false);
            break;
        case _NAV:
            oled_write_P(PSTR("Nav\n"), false);
            break;
        case _MOUSE:
            oled_write_P(PSTR("Mouse\n"), false);
            break;
        case _NUMBER:
            oled_write_P(PSTR("Number\n"), false);
            break;
        case _EXTRA:
            oled_write_P(PSTR("Extra\n"), false);
            break;
        case _SYMB:
            oled_write_P(PSTR("Symbol\n"), false);
            break;
        default:
            oled_write_P(PSTR("Undefined\n"), false);
    }
    // Host Keyboard LED Status
    uint8_t led_usb_state = host_keyboard_leds();
    oled_write_P(IS_LED_ON(led_usb_state, USB_LED_NUM_LOCK) ? PSTR("NUMLCK ") : PSTR("       "), false);
    oled_write_P(IS_LED_ON(led_usb_state, USB_LED_CAPS_LOCK) ? PSTR("CAPLCK ") : PSTR("       "), false);
    oled_write_P(IS_LED_ON(led_usb_state, USB_LED_SCROLL_LOCK) ? PSTR("SCRLCK ") : PSTR("       "), false);

}

bool oled_task_user(void) {
    if (is_keyboard_master()) {
       render_right(); // Renders the current keyboard state (layer, lock, caps, scroll, etc)
    } else {
        render_left();
    }
    return false;
}
